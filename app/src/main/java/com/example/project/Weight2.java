package com.example.project;

public class Weight2 {
    private float july;
    private float august;
    private float september;
    private float october;
    private float november;
    private float december;

    public Weight2(float july, float august, float september, float october, float november, float december) {
        this.july = july;
        this.august = august;
        this.september = september;
        this.october = october;
        this.november = november;
        this.december = december;
    }

    public float getJuly() {
        return july;
    }

    public float getAugust() {
        return august;
    }

    public float getSeptember() {
        return september;
    }

    public float getOctober() {
        return october;
    }

    public float getNovember() {
        return november;
    }

    public float getDecember() {
        return december;
    }
}
