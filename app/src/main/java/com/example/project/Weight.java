package com.example.project;

public class Weight {
    private float ja;
    private float fe;
    private float march;
    private float ap;
    private float may;
    private float june;

    public Weight(float ja, float fe, float march, float ap, float may, float june) {
        this.ja = ja;
        this.fe = fe;
        this.march = march;
        this.ap = ap;
        this.may = may;
        this.june = june;
    }

    public float getJa() {
        return ja;
    }

    public float getFe() {
        return fe;
    }

    public float getMarch() {
        return march;
    }

    public float getAp() {
        return ap;
    }


    public float getMay() {
        return may;
    }


    public float getJune() {
        return june;
    }

}
